The Commissioner Panchayati Raj and Rural Development has initiated Digital Panchayat ,
a Flagship G2C project, for Computerization and online issuance of citizen centric 
certificates. After a detailed system study by NIC, the department prioritized the
 applications to be automated for gram Panchayats. 
