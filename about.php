Trade Licenses, Property valuation, mutations, water tap connection, NOC, 
Building permission, Layout permissions and MNREGA. 
These applications provide an end to end solution for all the gram panchayats 
in the state. The software facilitates generation of certificates, payment gateway for 
citizens, eSign facility for panchayat functionaries, SMS and email alerts. 
